package com.mycompany.projeto.rabbit;

public class Usuario {

    private Integer idUsuario;
    private String nomeUsuario;
    private String emailUsuario;
    private Integer senhaUsuario;

    public Integer getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(Integer idUsuario) {
        this.idUsuario = idUsuario;
    }

    public String getNomeUsuario() {
        return nomeUsuario;
    }

    public void setNomeUsuario(String nomeUsuario) {
        this.nomeUsuario = nomeUsuario;
    }

    public String getEmailUsuario() {
        return emailUsuario;
    }

    public void setEmailUsuario(String emailUsuario) {
        this.emailUsuario = emailUsuario;
    }

    public Integer getSenhaUsuario() {
        return senhaUsuario;
    }

    public void setSenhaUsuario(Integer senhaUsuario) {
        this.senhaUsuario = senhaUsuario;
    }

    @Override
    public String toString() {
        return String.format("\nID: %d\nNOME: %s\nEMAIL: %s\nSENHA: %s\n",
                this.idUsuario, this.nomeUsuario, this.emailUsuario, this.senhaUsuario);
    }
}